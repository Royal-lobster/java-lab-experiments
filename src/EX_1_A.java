/**
 * write a java program to display default value of all primitive data types of
 * java.
 */

public class EX_1_A {
    static int v1;
    static long v2;
    static double v3;
    static String v4;
    static float v5;
    static boolean v6;
public static void main(String []args){
    System.out.println("the default value of int is " + v1);
    System.out.println("the default value of long is " + v2);
    System.out.println("the default value of double is " + v3);
    System.out.println("the default value of String is " + v4);
    System.out.println("the default value of float is " + v5);
    System.out.println("the default value of boolean is " + v6);
}
}